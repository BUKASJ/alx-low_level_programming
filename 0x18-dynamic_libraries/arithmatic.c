#include <stdio.h>

/**
 * add - add two integer
 * @a: integer 1
 * @b: integer 2
 *
 * Return: Sum of the numbers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: difference of the numbers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul  - multiplies two integer
 * @a: integer 1
 * @b: integer 2
 *
 * Return: Product of the numbers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div  - divides two integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: the numbers
 */
 
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod  - modulus two integer
 * @a: integer 1
 * @b: integer 2
 *
 * Return: modulus of the numbers
 */
 
int mod(int a, int b)
{
	return (a % b);
}
